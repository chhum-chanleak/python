# What is a variable?

# A variable is like a labeled box where you can store data to use later.

# You can store numbers, text, lists, or other types of data.
# You can change the value in the box anytime.

# Syntax: variable_name = value

# age = 25          # integer
# name = "Alex"     # string
# height = 1.75     # float
# is_student = True # boolean

# 25, "Alex", 1.75, and True are called <objects>.

# Rules for variable names

# 1. Can contain letters, numbers, and underscores (_)

# 2. Must start with a letter or underscore

# 3. Cannot be a Python keyword (if, for, print, etc.)

# 4. Case-sensitive (Age ≠ age)

# Good examples:

# first_name = "Alice"
# score1 = 100
# _total = 50

# Bad examples:

# 1name = "Bob"      # starts with number
# my-variable = 10   # contains hyphen
# for = 5            # keyword

# Using variables
# Assign

# x = 10
# y = 5

# # Use in expressions
# sum = x + y
# print(sum)  # Output: 15

# # Update variable
# x = x + 2
# print(x)    # Output: 12

# Python is dynamically typed

# You don’t need to declare the type.

# Python figures out the type from the value.

# var = 5        # var is an int
# var = "hello"  # now var is a string

